#include <unistd.h>

void	ft_putendl(char const *s)
{
	int		i;
	char	*str;

	i = 0;
	str = (char *)s;
	while (s[i] != '\0')
	{
		write (1, &s[i], 1);
		i++;
	}
	str[i] = '\n';
	write(1, &str[i], 1);
}
