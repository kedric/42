#include "libft.h"
#include <unistd.h>

int		ft_putnbr_fd(int n, int fd)
{
	if (n < 0)
	{
		n = -n;
		ft_putchar_fd('-', fd);
	}
	if (n > 9)
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
	}
	else
		if (ft_putchar_fd(48 + n, fd) == -1)
			return (-1);
	return (1);
}
