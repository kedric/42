#include "libft.h"
#include <stdlib.h>

char	*ft_strdup(const char *s1)
{
	int		size;
	char	*s2;

	if (s1)
	{
		size = ft_strlen(s1);
		if ((s2 = (char *)malloc(size * sizeof(char))) == NULL)
			return (NULL);
		ft_strcpy(s2, s1);
		return (s2);
	}
	return (NULL);
}
