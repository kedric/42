#include "libft.h"
#include <unistd.h>

int		ft_putnbr(int n)
{
	if (n < 0)
	{
		write(1, "-", 1);
		n = -n;
	}
	if (n > 9)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
	else
		if (ft_putchar(48 + n) == -1)
			return (-1);
	return (1);
}
