char	*ft_strcat(char	*s1, const char *s2)
{
	char	*tmp;

	if (s1 && s2)
	{
		tmp = s1;
		while (*tmp)
			tmp++;
		while ((*tmp++ = *s2++))
			;
		*tmp = '\0';
		return (s1);
	}
	return (s1);
}
