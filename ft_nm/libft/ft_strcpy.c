#include "libft.h"

char	*ft_strcpy(char *s1, const char *s2)
{
	char	*save;

	if (s1 && s2)
	{
		save = s1;
		while ((*s1++ = *s2++))
			;
		return (save);
	}
	return (NULL);
}
