int		ft_strcmp(const char *s1, const char *s2)
{
	char	*s12;
	char	*s22;

	if (s1 && s2)
	{
		s12 = (char *)s1;
		s22 = (char *)s2;
		while (*s12 == *s22 && *s12 && *s22)
		{
			s12++;
			s22++;
		}
		return (*s12 - *s22);
	}
	return (-1);
}
