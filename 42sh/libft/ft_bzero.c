#include <string.h>

void		ft_bzero(void *s, size_t n)
{
	size_t	i;

	i = 0;
	if (s != 0 && n > 0)
	{
		while (i < n)
		{
			*((char *)s + i) = '\0';
			i++;
		}
	}
}
